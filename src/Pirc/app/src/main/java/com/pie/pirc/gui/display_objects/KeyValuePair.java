package com.pie.pirc.gui.display_objects;

/**
 * Represents a key-value pair, supposed to be used by lists.
 *
 * Created by pgecsenyi on 2015.05.02..
 */
public class KeyValuePair
{
    /***************************************************************************************************************//**
     * Public fields.
     ******************************************************************************************************************/

    public String key;

    public String value;

    /***************************************************************************************************************//**
     * Constructor.
     ******************************************************************************************************************/

    public KeyValuePair(String key, String value)
    {
        this.key = key;
        this.value = value;
    }

    /***************************************************************************************************************//**
     * Getters and setters.
     ******************************************************************************************************************/

    public String getKey()
    {
        return key;
    }

    public String getValue()
    {
        return value;
    }

    /***************************************************************************************************************//**
     * Overridden Object methods.
     ******************************************************************************************************************/

    @Override
    public String toString()
    {
        return value;
    }
}
